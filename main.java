import java.util.*;

public class main{
	public static void main (String [] args){
		char switchvalue = 'b';
		
		switch(switchvalue){

			case 'a':
				System.out.println("value was a");
				break;
			
			case 'b':
				System.out.println("value was b");
				break;
			default:
				System.out.println("was neither a or b");
				break;

		}
	}

}
